public class Palavra {
    private String palavra;
    private String palavraCaixaAlta;
    private String palavraConcatenada;

    public String getPalavra() {
        return palavra;
    }

    public void setPalavra(String palavra) {
        this.palavra = palavra;
    }

    public String getPalavraCaixaAlta() {
        return palavraCaixaAlta;
    }

    public void setPalavraCaixaAlta(String palavraCaixaAlta) {
        this.palavraCaixaAlta = palavraCaixaAlta;
    }

    public String getPalavraConcatenada() {
        return palavraConcatenada;
    }

    public void setPalavraConcatenada(String palavraConcatenada) {
        this.palavraConcatenada = palavraConcatenada;
    }
}
